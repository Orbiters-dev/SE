def translate_run(text: str) -> str:
    """run.text 치환. 정확히 매칭되는 키 우선, 없으면 부분 치환 시도."""
    if text in TRANS:
        return TRANS[text]
    # 부분 치환: 긴 키부터 우선
    result = text
    for jp, kr in sorted(TRANS.items(), key=lambda x: -len(x[0])):
        if jp and jp in result:
            result = result.replace(jp, kr)
    return result


def main():
    print(f"원본: {SRC}")
    print(f"출력: {DST}")
    if not SRC.exists():
        print(f"ERROR: 원본 없음")
        return 1

    shutil.copy2(SRC, DST)
    print("파일 복사 완료")

    prs = Presentation(DST)
    changed = 0
    unchanged_samples = []

    for sidx, slide in enumerate(prs.slides, 1):
        for shape in slide.shapes:
            if not shape.has_text_frame:
                continue
            for para in shape.text_frame.paragraphs:
                for run in para.runs:
                    orig = run.text
                    if not orig.strip():
                        continue
                    new = translate_run(orig)
                    if new != orig:
                        run.text = new
                        changed += 1
                    elif any(ord(c) > 0x3040 for c in orig):
                        # 일본어 히라가나/카타카나/한자 있는데 번역 안 된 것
                        if len(unchanged_samples) < 20:
                            unchanged_samples.append(f"s{sidx}: {orig!r}")

    prs.save(DST)
    print(f"\n치환 완료: {changed}개 run")
    if unchanged_samples:
        print(f"\n⚠ 번역 누락 의심 ({len(unchanged_samples)}건):")
        for s in unchanged_samples:
            print(f"  {s}")
    print(f"\n저장: {DST}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
